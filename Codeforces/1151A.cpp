#include<bits/stdc++.h>
using namespace std;

typedef long long int ll;

ll cha(char a,char b)
{
    return min((a-b+26)%26,(b-a+26)%26);
}

int main()
{
    ios::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    ll n;
    cin>>n;
    char s[n+1];
    cin>>s;
    ll cost=1e12;
    for(int i=0;i<n-3;i++)
    {
        ll c=0;
        c+=cha(s[i],'A');
        c+=cha(s[i+1],'C');
        c+=cha(s[i+2],'T');
        c+=cha(s[i+3],'G');

        cost=min(cost,c);
    }
    cout<<cost<<endl;

}